        SUBROUTINE ROTACHO(TEMP,NV,NJ)
        IMPLICIT REAL*8 (A-H,O-Z)
          double precision ::xrand(3)        
            DIMENSION pJT(47)
        
!       ***CONSTANTES OBTIDAS DE CODATA 1998 >> SKBOLT =k/hc(cm-1K-1)
        SKBOLT =.6950356D0
!       *BE E ALPHAE RETIRADAS DA PAG HTTP://WEBBOOK.NIST.GOV/CHEMISTRY*      

        BE=18.871d0
        ALPHAE=0.714d0
        BPARAM=BE-ALPHAE*(NV+.5)
        JMIN=0
        SKTROT=SKBOLT*TEMP
        SKBROT=BPARAM/SKTROT
        FPART=0.0D0
        DO 8361 I=1,47
        J=I-1
        pjt(i)=(2.D0*J+1.D0)*EXP(-J*(J+1.D0)*SKBROT)
        FPART=FPART+pjt(i)    
        IF(ABS(pjt(i)).LT.1.D-5)GO TO 10
8361    CONTINUE
10      JMAX=I
!        write(6,*)JMAX
        call random_number(xrand)      
        U=xrand(1)
        prob=0.0d0
        DO 8362 I=1,47
        prob=prob+pjt(i)/fpart
        IF(prob.gT.u)GO TO 20
8362    CONTINUE
20      nj=i-1
        RETURN
        END


