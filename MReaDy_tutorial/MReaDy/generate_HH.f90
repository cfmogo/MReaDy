subroutine generate_HH(igen,xg,yg,zg,vxg,vyg,vzg)
use variables
use phys_parameters
use sim_variables
use constants
use sim_variables
use phys_parameters

implicit none

!-----for positioning---------
double precision ,intent(out):: xg(2),yg(2),zg(2)
double precision    ::     rxij,ryij,rzij,delta_r_atoms,distmini
double precision     ::    phi,cosphi,sinphi,costeta,sinteta
double precision     ::     rm,distx,disty,distz
integer     ::     n_vib,j_rot
integer     ::     i

!----for CM velocities
double precision     ::    phiCM,cosphiCM,sinphiCM,costetaCM,sintetaCM,velCM,Ma,Mb
double precision     ::    modvelocity,vxgCM,vygCM,vzgCM
double precision,intent(out)     ::      vxg(2),vyg(2),vzg(2)
integer,intent(in)      ::     igen
double precision     ::     xrand(3)

!----for rovibrational velocities
!----------vibration------------------
double precision     ::     tvib
double precision     ::     RIN(38,15),ROU(38,15),TPT(38,15)
double precision     ::     eta,sineta,coseta

!----------rotation-------------------
double precision     ::     L_ang,v1,v2

!----integration for the vibration motion-------
double precision     ::     rxgold(2),rygold(2),rzgold(2)

integer    ::     t_int

COMMON /DISTRIHH/ RIN,ROU,TPT

! HH molecule parameters
Ma=massH11*uma
Mb=Ma

!    Determine a random point inside a square box of lengh boxl
1   call random_number(xrand)

xg(1)=xrand(1)*boxl
yg(1)=xrand(2)*boxl
zg(1)=xrand(3)*boxl

!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
!       print*,'change in genHH'
!
!       xg(1)=boxl*0.13d0
!       yg(1)=boxl*0.56d0
!       zg(1)=boxl*0.5d0
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!

! To asure that points are not overlapped
if (igen.gt.1) then

!       print*,'change in secondgenHH'
!       xg(1)=boxl*0.3d0
!       yg(1)=boxl*0.5d0
!       zg(1)=boxl*0.5d0

do i=1,igen-1

!determine distance between points
call mod_dist(xg(1),yg(1),zg(1),rx(i),ry(i),rz(i),rxij,ryij,rzij,delta_r_atoms)

!minimum distance between the new point and remaining atoms
distmini=6E-10

!if deltar_r_atoms is less than distmini

if(delta_r_atoms.lt.distmini) then
goto 1 ! generates a new position
end if
enddo
end if

! determining the vibration and rotational initial states
call vibraHH(temp,n_vib)
call ROTACHH(temp,n_vib,j_rot)
n_vib=0
j_rot=0

!With _rot and n_vib the distance between H atoms and half period of
!vibration cicle is (tables given in same file of vibraHH and ROTACHH):
rm=(rou(j_rot+1,n_vib+1)*1D-10)!*(1.0d0+0.3d0)
tvib=tpt(j_rot+1,n_vib+1)

!generaton of a random direction vector for the second atom

call random_number(xrand)

phi=2.0d0*pi*xrand(1)
cosphi=cos(phi)
sinphi=sin(phi)

costeta=1.0d0-2.0d0*xrand(2)
sinteta=sqrt(1.0d0-costeta**2)

distx=rm*sinteta*cosphi
disty=rm*sinteta*sinphi
distz=rm*costeta

!final atribution of the position of both atoms
!initial positions       |*|    !final positions
!--04--xHH            |*|    !-------14-------xHH
!--------16--------xH1   |*|    !---------18---------xH1
xg(1)=xg(1)+0.5d0*distx
yg(1)=yg(1)+0.5d0*disty
zg(1)=zg(1)+0.5d0*distz
xg(2)=xg(1)-distx
yg(2)=yg(1)-disty
zg(2)=zg(1)-distz


!------------------\\----------//-----------------------------
!-------adding the rotational velocity------------------------
!------------------\\----------//-----------------------------

! j_rot is determined in function ROTACHH()
L_ang=sqrt(dble(j_rot*(j_rot+1)))*hbar

v1=L_ang/Ma/rm
v2=L_ang/Mb/rm

call random_number(xrand)

eta=2.0d0*pi*xrand(1)
coseta=cos(eta)
sineta=sin(eta)

vxg(1)=v1*(sinphi*coseta-costeta*cosphi*sineta)
vyg(1)=-v1*(cosphi*coseta+costeta*sinphi*sineta)
vzg(1)=v1*sinteta*sineta

vxg(2)=-vxg(1)
vyg(2)=-vyg(1)
vzg(2)=-vzg(1)

!--------------determination of the old positioning-------
!----------------needed for  integration------------------

rxgold(1)=xg(1)-vxg(1)*dt
rygold(1)=yg(1)-vyg(1)*dt
rzgold(1)=zg(1)-vzg(1)*dt

rxgold(2)=xg(2)-vxg(2)*dt
rygold(2)=yg(2)-vyg(2)*dt
rzgold(2)=zg(2)-vzg(2)*dt
!---------determination of the total integration steps-------

call random_number(xrand)

t_int=nint(xrand(1)*tvib*2.0d0/dt)

call verlet_HH(t_int,rxgold,rygold,rzgold,xg,yg,zg,vxg,vyg,vzg)

!---------------------------------------------------------
!------------------------/for CM velocity/----------------
!---------------------------------------------------------
ekin=0.0d0

select case (temp_distribution)

case (0)
modvelocity=sqrt(3*rkbolt*temp/(Ma+Mb))

case (1)
call velocity_dist_HH(temp,velCM)
modvelocity = velCM !for HH!!!! Change later for OH and O2
case default
stop 'error in selection of Kinetic Energy distribution'
end select

call random_number(xrand)

phiCM=2.0d0*pi*xrand(1)
cosphiCM=cos(phiCM)
sinphiCM=sin(phiCM)

costetaCM=1.0d0-2.0d0*xrand(2)
sintetacM=sqrt(1.0d0-costetaCM**2)

vxgCM=modvelocity*sintetaCM*cosphiCM
vygCM=modvelocity*sintetaCM*sinphiCM
vzgCM=modvelocity*costetaCM

!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
!    print*,'change genHH'
!    vxgCM=modvelocity
!    vygCM=0.0d0
!    vzgCM=0.0d0
!
!    if (igen.gt.1) then
!    vxgCM=modvelocity
!    vygCM=0.0d0
!    vzgCM=0.0d0
!    end if
!!!!!!!!!!!!!!!!!!!!!!!!!1

vxg(1)=vxg(1)+vxgCM
vyg(1)=vyg(1)+vygCM
vzg(1)=vzg(1)+vzgCM

vxg(2)=vxg(2)+vxgCM
vyg(2)=vyg(2)+vygCM
vzg(2)=vzg(2)+vzgCM

end subroutine generate_HH

subroutine velocity_dist_HH(temperature,v)
use constants
implicit none

double precision, intent(in):: temperature
double precision, intent(out):: v
double precision ::value,pv,mass_tot
double precision ::xrand(3)

mass_tot=2*massH11*uma

rej:    do
call random_number(xrand)
value = (xrand(1)/(1.0d0-xrand(1)))
pv=value*exp(-value)*1.0d0/sqrt((1.0d0-xrand(1))**(3)*xrand(1))

if (xrand(2)*1.8d0.le.pv) exit rej

end do rej

v=sqrt(2.0d0*rkbolt*temperature*value/mass_tot)

end subroutine velocity_dist_HH



SUBROUTINE verlet_HH(tg,xgold,ygold,zgold,xg,yg,zg,vxg,vyg,vzg)
use constants
use variables
use phys_parameters
use sim_variables
use sim_variables
use phys_parameters

implicit none

double precision :: dpotxg(2),dpotyg(2),dpotzg(2)
double precision :: dtsq,xgnew(2),ygnew(2),zgnew(2),dt2
double precision :: xg(2),yg(2),zg(2)
double precision :: massg(2)
double precision :: xgold(2),ygold(2),zgold(2)
double precision, intent(out) :: vxg(2),vyg(2),vzg(2)

integer :: t,tg

massg=massH11*uma

call potential_HH(xg,yg,zg,dpotxg,dpotyg,dpotzg)

time:   do t = 1, tg

dtsq=(dt)**2
dt2=2.0d0*dt

xgnew    = 2.0d0 * xg -xgold - dtsq * dpotxg/massg
ygnew    = 2.0d0 * yg -ygold - dtsq * dpotyg/massg
zgnew    = 2.0d0 * zg -zgold - dtsq * dpotzg/massg

vxg= ( xgnew - xgold ) / dt2
vyg= ( ygnew - ygold ) / dt2
vzg= ( zgnew - zgold ) / dt2

xgold=xg
ygold=yg
zgold=zg

xg=xgnew
yg=ygnew
zg=zgnew


!-------------periodic bounderies-----------------------------------------------
!------------relocate points back inside the box--------------------------------    !

!best proposal is to make direct calculation

xgold = xgold - boxl*floor(xg/boxl)
ygold = ygold - boxl*floor(yg/boxl)
zgold = zgold - boxl*floor(zg/boxl)

xg = xg - boxl*floor(xg/boxl)
yg = yg - boxl*floor(yg/boxl)
zg = zg - boxl*floor(zg/boxl)

call potential_HH(xg,yg,zg,dpotxg,dpotyg,dpotzg)

end do time

END SUBROUTINE verlet_HH

subroutine potential_HH(rxvib,ryvib,rzvib,dpotxvib,dpotyvib,dpotzvib)
use constants
use variables
use phys_parameters
use sim_variables

implicit none

double precision, intent(in) :: rxvib(2),ryvib(2),rzvib(2)

double precision, intent(out) :: dpotxvib(2),dpotyvib(2),dpotzvib(2)
double precision    :: rxij, ryij, rzij,V,dV
double precision    :: rijsq,rij

rxij = rxvib(1) - rxvib(2)
ryij = ryvib(1) - ryvib(2)
rzij = rzvib(1) - rzvib(2)


!for setting the minimum image convention
rxij = rxij -anint(rxij/boxl)*boxl
ryij = ryij -anint(ryij/boxl)*boxl
rzij = rzij -anint(rzij/boxl)*boxl


!for setting the cutoff radius
!Determine the vector between the ij particles
rijsq = rxij**2 +  ryij**2 +  rzij**2
rij= sqrt(rijsq)

! verificar a chamada

call pothhs_f1(rij,V,dV)

dpotxvib(1)=  + dV*rxij/rij
dpotyvib(1)=  + dV*ryij/rij
dpotzvib(1)=  + dV*rzij/rij

dpotxvib(2)=  - dpotxvib(1)
dpotyvib(2)=  - dpotyvib(1)
dpotzvib(2)=  - dpotzvib(1)

end subroutine potential_HH


SUBROUTINE VIBRAHH(TEMP,NV)
IMPLICIT double precision (A-H,O-Z)
double precision ::xrand(3)
integer :: nv,I

DIMENSION pv(15)
!       *CALCULADA A PARTIR DA PAG HTTP://WEBBOOK.NIST.GOV/CHEMISTRY*
!        SKHARM= 575.10887D0
!       ************CONSTANTES OBTIDAS DE CODATA 1998**********
!        print*,'temp',temp
SHBAR = 1.054571596D-34
SKBOLT= 1.3806503D-23
cm1=1.986445440000000d-023
we=4401.213d0
WEXE=121.336D0
SKTVIB=1.0D0/(SKBOLT* TEMP)
FPART=0.0D0
DO 8361 I=1,15
EV=(WE*(I-0.5D0)-WEXE*(I-0.5D0)**2)*CM1
pv(i)=exp(-EV*sktvib)
FPART=FPART+PV(I)
!        print*,i,pv(i),fpart
IF(ABS(pv(i)).LT.(1.0d-7))GO TO 10
8361    CONTINUE
10      vMAX=I

call random_number(xrand)
U=xrand(1)
!        print*,'U',U
prob=0.0d0
DO 8362 I=1,15
prob=prob+pV(i)/fpart
!        print*,'i,U,prob',i,U,prob
IF(prob.gT.u)GO TO 20
8362    CONTINUE
20      nV=i-1
RETURN

END

SUBROUTINE ROTACHH(TEMP,NV,NJ)
implicit integer (i-n)
IMPLICIT double precision (A-H,O-Z)
double precision ::xrand(3)
DIMENSION pJT(38)


!       ***CONSTANTES OBTIDAS DE CODATA 1998 >> SKBOLT =k/hc(cm-1K-1)
SKBOLT =.6950356D0
!       *BE E ALPHAE RETIRADAS DA PAG HTTP://WEBBOOK.NIST.GOV/CHEMISTRY*
BE=60.8530d0
ALPHAE=3.0622d0
BPARAM=BE-ALPHAE*(NV+.5)
JMIN=0
SKTROT=SKBOLT*TEMP
SKBROT=BPARAM/SKTROT
FPART=0.0D0
DO 8361 I=1,38
J=I-1
fp=2.0d0*mod(j,2)+1.0d0
pjt(i)=fp*(2.D0*J+1.D0)*EXP(-J*(J+1.D0)*SKBROT)
FPART=FPART+pjt(i)
IF(ABS(pjt(i)).LT.1.D-5)GO TO 10
8361    CONTINUE
10      JMAX=I
!        write(6,*)JMAX
call random_number(xrand)
U=xrand(1)
prob=0.0d0
DO 8362 I=1,38
prob=prob+pjt(i)/fpart
IF(prob.gT.u)GO TO 20
8362    CONTINUE
20      nj=i-1
RETURN
END


