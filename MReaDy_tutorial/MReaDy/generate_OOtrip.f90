if (nv.gt.20) nv=20
RETURN
END


SUBROUTINE ROTACOOt(TEMP,NV,NJ)
IMPLICIT double precision (A-H,O-Z)
implicit integer(i-n)
double precision ::xrand(3)


SKBOLT =  .695030400466047D0
BE=.1444756E1
ALPHAE=.1746304E-1
BPARAM=BE-ALPHAE*(NV+.5)
JMIN=0
SKTROT=SKBOLT*TEMP
SKBROT=BPARAM/SKTROT
DO 8361 I=2,152
J=I-1
