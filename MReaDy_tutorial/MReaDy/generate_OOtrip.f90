SUBROUTINE VIBRAOOt(TEMP,NV)
IMPLICIT double precision (A-H,O-Z)
implicit integer(i-n)
double precision ::xrand(3)


SKHARM=  1171.856D0
SHBAR = 1.054588757D-34
SKBOLT= 1.38066244D-23
SMUBC=  1.3284523095D-26
SEVIBQ=SHBAR*SQRT(SKHARM/SMUBC)
SKTVIB=SKBOLT* TEMP

call random_number(xrand)

SRAND=xrand(1)
SEVIB=-SKTVIB*LOG(SRAND)
SNVIB=SEVIB/SEVIBQ
